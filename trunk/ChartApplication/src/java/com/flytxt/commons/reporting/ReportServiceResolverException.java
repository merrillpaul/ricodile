package com.flytxt.commons.reporting;

/**
 *
 * @author merrill.paul
 */
public class ReportServiceResolverException extends Exception {

    /**
     * Creates a new instance of <code>ReportServiceResolverException</code> without detail message.
     */
    public ReportServiceResolverException() {
    }

    public ReportServiceResolverException(Throwable cause) {
        super(cause);
    }

    public ReportServiceResolverException(String message, Throwable cause) {
        super(message, cause);
    }

    public ReportServiceResolverException(String message) {
        super(message);
    }

    
}

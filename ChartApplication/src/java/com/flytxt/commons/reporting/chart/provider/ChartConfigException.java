package com.flytxt.commons.reporting.chart.provider;

/**
 *
 * @author merrill.paul
 */
public class ChartConfigException extends Exception {

    /**
     * Creates a new instance of <code>ChartConfigException</code> without detail message.
     */
    public ChartConfigException() {
    }

    public ChartConfigException(Throwable cause) {
        super(cause);
    }

    public ChartConfigException(String message, Throwable cause) {
        super(message, cause);
    }

    public ChartConfigException(String message) {
        super(message);
    }

  
}
